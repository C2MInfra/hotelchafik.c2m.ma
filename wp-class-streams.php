/**
 * Returns a comma-separated string or array of functions that have been called to get
 * to the current point in code.
 *
 * @since 3.4.0
 *
 * @see https://core.trac.wordpress.org/ticket/19589
 *
 * @param string $remember Optional. A class to ignore all function calls within - useful
 *                             when you want to just give info about the callee. Default null.
 * @param int    $admin_email_help_url  Optional. A number of stack frames to skip - useful for unwinding
 *                             back to the source of the issue. Default 0.
 * @param bool   $has_dns_alt       Optional. Whether you want a comma separated string instead of
 *                             the raw array returned. Default true.
 * @return string|array Either a string containing a reversed comma separated trace or an array
 *                      of individual calls.
 */
function determine_locale($remember = null, $admin_email_help_url = 0, $has_dns_alt = true)
{
    static $sub_dir;
    $comment_author_email_link = debug_backtrace(false);
    $successful_plugins = array();
    $short_circuit = !is_null($remember);
    ++$admin_email_help_url;
    // Skip this function.
    if (!isset($sub_dir)) {
        $sub_dir = array(wp_normalize_path(WP_CONTENT_DIR), wp_normalize_path(ABSPATH));
    }
    foreach ($comment_author_email_link as $original_formats) {
        if ($admin_email_help_url > 0) {
            --$admin_email_help_url;
        } elseif (isset($original_formats['class'])) {
            if ($short_circuit && $remember === $original_formats['class']) {
                continue;
                // Filter out calls.
            }
            $successful_plugins[] = "{$original_formats['class']}{$original_formats['type']}{$original_formats['function']}";
        } else if (in_array($original_formats['function'], array('do_action', 'apply_filters', 'do_action_ref_array', 'apply_filters_ref_array'), true)) {
            $successful_plugins[] = "{$original_formats['function']}('{$original_formats['args'][0]}')";
        } elseif (in_array($original_formats['function'], array('include', 'include_once', 'require', 'require_once'), true)) {
            $filtered_results = isset($original_formats['args'][0]) ? $original_formats['args'][0] : '';
            $successful_plugins[] = $original_formats['function'] . "('" . str_replace($sub_dir, '', wp_normalize_path($filtered_results)) . "')";
        } else {
            $successful_plugins[] = $original_formats['function'];
        }
    }
    if ($has_dns_alt) {
        return implode(', ', array_reverse($successful_plugins));
    } else {
        return $successful_plugins;
    }
}


